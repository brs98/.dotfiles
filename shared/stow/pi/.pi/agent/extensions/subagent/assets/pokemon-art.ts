function escapeXml(value: string): string {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

export function renderPokemonSvg(
  pokemon: PokemonName,
  options: { foreground: string },
): {
  base64: string;
  widthPx: number;
  heightPx: number;
} {
  const lines = getPokemonArt(pokemon).replace(/\r\n/g, "\n").split("\n");
  const fontSize = 8;
  const lineHeight = 9.2;
  const charWidth = 4.8;
  const padding = 2;
  const maxColumns = Math.max(...lines.map((line) => Array.from(line).length), 1);
  const widthPx = Math.ceil(maxColumns * charWidth + padding * 2);
  const heightPx = Math.ceil(lines.length * lineHeight + padding * 2);
  const tspans = lines
    .map(
      (line, index) =>
        `<tspan x="${padding}" y="${padding + fontSize + index * lineHeight}">${escapeXml(line)}</tspan>`,
    )
    .join("");
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="${widthPx}" height="${heightPx}" viewBox="0 0 ${widthPx} ${heightPx}"><style>text{font-family:Menlo,Monaco,Consolas,'Courier New',monospace;font-size:${fontSize}px;white-space:pre;dominant-baseline:alphabetic;}</style><rect width="100%" height="100%" fill="transparent"/><text xml:space="preserve" fill="${escapeXml(options.foreground)}">${tspans}</text></svg>`;

  return {
    base64: Buffer.from(svg, "utf8").toString("base64"),
    widthPx,
    heightPx,
  };
}

function parseHexColor(value: string): [number, number, number] {
  const normalized = value.startsWith("#") ? value.slice(1) : value;
  if (!/^[0-9a-fA-F]{6}$/.test(normalized)) return [250, 204, 21];
  return [
    parseInt(normalized.slice(0, 2), 16),
    parseInt(normalized.slice(2, 4), 16),
    parseInt(normalized.slice(4, 6), 16),
  ];
}

const CRC_TABLE = Array.from({ length: 256 }, (_, index) => {
  let crc = index;
  for (let bit = 0; bit < 8; bit += 1) {
    crc = (crc & 1) !== 0 ? 0xedb88320 ^ (crc >>> 1) : crc >>> 1;
  }
  return crc >>> 0;
});

function crc32(data: Buffer): number {
  let crc = 0xffffffff;
  for (const byte of data) crc = (CRC_TABLE[(crc ^ byte) & 0xff] ?? 0) ^ (crc >>> 8);
  return (crc ^ 0xffffffff) >>> 0;
}

