function hashString(value: string): number {
  let hash = 2166136261;
  for (let i = 0; i < value.length; i += 1) {
    hash ^= value.charCodeAt(i);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

export function choosePokemonForSubagent(params: {
  task?: string;
  role?: string;
  model?: string;
  cwd?: string;
}): PokemonName {
  const role = params.role?.toLowerCase() ?? "";
  const task = params.task?.toLowerCase() ?? "";
  const haystack = `${role} ${task}`;

  if (/review|critic|audit|security/.test(haystack)) return "Gengar";
  if (/plan|architect|design|strategy/.test(haystack)) return "Mewtwo";
  if (/implement|worker|build|code|fix/.test(haystack)) return "Charizard";
  if (/research|scout|explore|investigate|find/.test(haystack)) return "Pikachu";
  if (/test|qa|verify|diagnos/.test(haystack)) return "Eevee";
  if (/doc|write|explain|summar/.test(haystack)) return "Jigglypuff";
  if (/slow|large|deep|thorough/.test(haystack)) return "Snorlax";

  const seed = `${params.role ?? ""}\n${params.task ?? ""}\n${params.model ?? ""}\n${params.cwd ?? ""}`;
  return POKEMON_ROSTER[hashString(seed) % POKEMON_ROSTER.length] ?? "Pikachu";
}

function isBlankChar(ch: string): boolean {
  return ch === " " || ch === "\t" || ch === BRAILLE_BLANK;
}

function brailleMask(ch: string): number {
  const code = ch.codePointAt(0);
  if (code === undefined) return 0;
  if (code >= BRAILLE_BASE && code <= BRAILLE_LAST) return code - BRAILLE_BASE;
  return isBlankChar(ch) ? 0 : 0xff;
}

function artToPixels(art: string): PixelGrid {
  const sourceLines = art.replace(/\r\n/g, "\n").split("\n");
  const lineChars = sourceLines.map((line) => Array.from(line));
  const sourceWidth = Math.max(...lineChars.map((line) => line.length), 0);
  const pixels: PixelGrid = Array.from({ length: sourceLines.length * 4 }, () =>
    Array.from({ length: sourceWidth * 2 }, () => false),
  );

  for (let lineIndex = 0; lineIndex < lineChars.length; lineIndex += 1) {
    const chars = lineChars[lineIndex] ?? [];
    for (let charIndex = 0; charIndex < sourceWidth; charIndex += 1) {
      const mask = brailleMask(chars[charIndex] ?? BRAILLE_BLANK);
      for (let y = 0; y < 4; y += 1) {
        for (let x = 0; x < 2; x += 1) {
          const bit = BRAILLE_DOT_BITS[y]?.[x] ?? 0;
          const row = pixels[lineIndex * 4 + y];
          if (row && (mask & bit) !== 0) row[charIndex * 2 + x] = true;
        }
      }
    }
  }

  return cropPixels(pixels);
}

function cropPixels(pixels: PixelGrid): PixelGrid {
  let minX = Number.POSITIVE_INFINITY;
  let minY = Number.POSITIVE_INFINITY;
  let maxX = -1;
  let maxY = -1;

  for (let y = 0; y < pixels.length; y += 1) {
    const row = pixels[y] ?? [];
    for (let x = 0; x < row.length; x += 1) {
      if (!row[x]) continue;
      minX = Math.min(minX, x);
      minY = Math.min(minY, y);
      maxX = Math.max(maxX, x);
      maxY = Math.max(maxY, y);
    }
  }

  if (maxX < 0 || maxY < 0) return [];

  return pixels.slice(minY, maxY + 1).map((row) => row.slice(minX, maxX + 1));
}

function sampleBlock(pixels: PixelGrid, startX: number, startY: number, scale: number): boolean {
  let filled = 0;
  let total = 0;
  for (let y = startY; y < Math.min(startY + scale, pixels.length); y += 1) {
    const row = pixels[y] ?? [];
    for (let x = startX; x < Math.min(startX + scale, row.length); x += 1) {
      total += 1;
      if (row[x]) filled += 1;
    }
  }

  return filled >= Math.max(1, Math.ceil(total * 0.12));
}

function downsample(pixels: PixelGrid, scale: number): PixelGrid {
  const sourceHeight = pixels.length;
  const sourceWidth = pixels[0]?.length ?? 0;
  const height = Math.ceil(sourceHeight / scale);
  const width = Math.ceil(sourceWidth / scale);

  return Array.from({ length: height }, (_, y) =>
    Array.from({ length: width }, (_, x) => sampleBlock(pixels, x * scale, y * scale, scale)),
  );
}

function pixelsToBraille(pixels: PixelGrid): string[] {
  const height = pixels.length;
  const width = pixels[0]?.length ?? 0;
  const rows = Math.ceil(height / 4);
  const cols = Math.ceil(width / 2);
  const lines: string[] = [];

  for (let rowIndex = 0; rowIndex < rows; rowIndex += 1) {
    const chars: string[] = [];
    for (let colIndex = 0; colIndex < cols; colIndex += 1) {
      let mask = 0;
      for (let y = 0; y < 4; y += 1) {
        for (let x = 0; x < 2; x += 1) {
          if (pixels[rowIndex * 4 + y]?.[colIndex * 2 + x]) {
            mask |= BRAILLE_DOT_BITS[y]?.[x] ?? 0;
          }
        }
      }
      chars.push(String.fromCharCode(BRAILLE_BASE + mask));
    }
    lines.push(trimBrailleEnd(chars.join("")));
  }

  return trimEmptyRows(lines);
}

function trimBrailleEnd(line: string): string {
  let chars = Array.from(line);
  while (chars.at(-1) === BRAILLE_BLANK) chars = chars.slice(0, -1);
  return chars.join("");
}

function trimEmptyRows(lines: string[]): string[] {
  const isEmpty = (line: string) =>
    Array.from(line).every((ch) => ch === BRAILLE_BLANK || ch === " ");
  let start = 0;
  let end = lines.length;
  while (start < end && isEmpty(lines[start] ?? "")) start += 1;
  while (end > start && isEmpty(lines[end - 1] ?? "")) end -= 1;
  return lines.slice(start, end);
}

export function renderPokemonArt(
  pokemon: PokemonName,
  options: { maxColumns: number; maxRows: number },
): string[] {
  const pixels = artToPixels(POKEMON_ART[pokemon]);
  const sourceWidth = pixels[0]?.length ?? 1;
  const sourceHeight = pixels.length || 1;
  const maxPixelWidth = Math.max(2, options.maxColumns * 2);
  const maxPixelHeight = Math.max(4, options.maxRows * 4);
  const scale = Math.max(
    1,
    Math.ceil(sourceWidth / maxPixelWidth),
    Math.ceil(sourceHeight / maxPixelHeight),
  );

  return pixelsToBraille(downsample(pixels, scale)).slice(0, options.maxRows);
}

